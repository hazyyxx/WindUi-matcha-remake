-- Lazy-load a name->rbxassetid map from the Footagesus/Icons repo
local function loadIconSet(setName)
	if _iconSets[setName]~=nil then return _iconSets[setName] or nil end
	local set
	pcall(function()
		set = loadstring(game:HttpGet("https://raw.githubusercontent.com/Footagesus/Icons/refs/heads/main/"..setName.."/dist/Icons.lua"))()
	end)
	_iconSets[setName] = set or false
	return _iconSets[setName] or nil
end
-- Optional: host your own icon PNGs and point names at them:
--   MatchaUI.IconBaseUrl = "https://raw.githubusercontent.com/you/icons/main/"
--   then Icon="house" -> that base .. "house" .. IconExt (default ".png")
MatchaUI.IconBaseUrl = "https://raw.githubusercontent.com/hazyyxx/WindUi-matcha-remake/main/icons/"
MatchaUI.IconExt = ".png"
-- Remote game-status JSON (edit this file in your own repo to control statuses).
-- Format: { "Blade Ball": "working", "Fallen Survival": "dev", "Old Game": false }
MatchaUI.StatusSource = "https://raw.githubusercontent.com/hazyyxx/WindUi-matcha-remake/main/status.json"
local function resolveIcon(name)
	local setName,iconName = name:match("^([%w_]+):(.+)$")
	if not iconName then iconName = name end
	if MatchaUI.IconBaseUrl then
		return MatchaUI.IconBaseUrl .. iconName .. (MatchaUI.IconExt or ".png")
	end
	-- fallback: Footagesus rbxassetid map (only renders if the build can fetch asset bytes)
	local set = loadIconSet(setName or MatchaUI.IconSet)
	if type(set)=="table" then
		local v = set[iconName]
		if type(v)=="string" then return v end
	end
	return nil
end
-- Fetch raw image bytes for a Roblox asset id (handles Decal->Image indirection)
local function fetchAssetBytes(id)
	local data
	pcall(function() data = game:HttpGet("https://assetdelivery.roblox.com/v1/asset/?id="..id) end)
	if type(data)=="string" and data:find("<roblox",1,true) then
		local inner = data:match("rbxassetid://(%d+)") or data:match("[?&]id=(%d+)")
		if inner and inner~=id then pcall(function() data = game:HttpGet("https://assetdelivery.roblox.com/v1/asset/?id="..inner) end) end
	end
	return data
end
local function imgData(src)
	if not src or src=="" then return nil end
	if _imgCache[src]~=nil then return _imgCache[src] or nil end
	local data
	if src:match("^https?://") then pcall(function() data = game:HttpGet(src) end)
	elseif isfile and isfile(src) then pcall(function() data = readfile(src) end)
	else data = src end
	_imgCache[src] = data or false
	return data
end
-- Validate that a byte string is actually an image (PNG/JPG/GIF/BMP/WEBP)
local function looksImage(b)
	if type(b)~="string" or #b<8 then return false end
	return b:sub(1,4)=="\137PNG" or b:sub(1,2)=="\255\216" or b:sub(1,3)=="GIF"
		or b:sub(1,2)=="BM" or b:sub(1,4)=="RIFF"
end
