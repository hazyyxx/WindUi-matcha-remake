					cy=cy+elH+7
				end  -- elements
				cy=cy+4
			end  -- sections

			tab._scrollMax=math.max(0,cy-(WH-C.TH)+BOTPAD)
			if tab._active then win._scrollMax=tab._scrollMax end
			tab:_refreshContentPos()
			tab:_refreshContentHbs()
			win:_updateScrollbar()
		end  -- tab:_build

		-- First tab auto-activates and builds
		if idx==1 then
			win._active=tab; tab._active=true
			tab._btn.Color=lighten(T.Background,.10); tab._btx.Color=T.Text
			if tab._abar then tab._abar.Visible=true end
			task.spawn(function() task.wait(); tab:_build() end)
		end

		return tab
	end  -- win:Tab

	-- Section shorthand on window (returns group proxy with :Tab)
	function win:Section(cfg2)
		local lbl=type(cfg2)=="string" and cfg2 or (cfg2 and cfg2.Title or "")
		local g={_tabs={}}
		function g:Tab(c) local t=win:Tab(c); g._tabs[#g._tabs+1]=t; return t end
		for _,m in ipairs({"Toggle","Slider","Dropdown","Button","Keybind","Input","Colorpicker","Space","Text"}) do
			local mm=m
			g[mm]=function(self,c)
				if #g._tabs==0 then g:Tab({Title=lbl}) end
				return g._tabs[#g._tabs][mm](g._tabs[#g._tabs],c)
			end
		end
		return g
	end

	-- Live theme: recolor chrome, rebuild content drawings with current theme
	function win:_applyTheme()
		local T2=MatchaUI.Theme
		pcall(function()
			wBrd.Color=darken(T2.Background,.5); wBg.Color=T2.Background
			wBar.Color=T2.Background; wBarB.Color=T2.Background; wTtx.Color=T2.Text; wTbSep.Color=lighten(T2.Background,.10)
			wSide.Color=lighten(T2.Background,.022); wSLn.Color=lighten(T2.Background,.07); wCont.Color=T2.Background; wBotMask.Color=T2.Background
			wMnBg.Color=lighten(T2.Background,.10); wMnTx.Color=T2.Text
			wSbThumb.Color=lighten(T2.Background,.12); wTipBg.Color=lighten(T2.Background,.06); wTipTx.Color=T2.Text
		end)
		if win._titleIco then pcall(function() win._titleIco.Color=T2.Text end) end
		for _,t in ipairs(win._tabs) do
			if t._active then pcall(function() t._btn.Color=lighten(T2.Background,.10); t._btx.Color=T2.Text; if t._bico then t._bico.Color=T2.Text end; if t._abar then t._abar.Color=T2.Slider; t._abar.Visible=not win._hidden end end)
			else pcall(function() t._btn.Color=lighten(T2.Background,.022); t._btx.Color=T2.Placeholder; if t._bico then t._bico.Color=T2.Placeholder end; if t._abar then t._abar.Color=T2.Slider; t._abar.Visible=false end end) end
		end
		-- rebuild content drawings (they carry baked-in colors)
		local contentSet={}
		for _,t in ipairs(win._tabs) do
			pcall(function() t:_closePopups() end)
			for _,d in ipairs(t._tdraws) do contentSet[d]=true end
		end
		for d in pairs(contentSet) do pcall(function()d:Remove()end) end
		local na={}
		for _,d in ipairs(win._all) do if not contentSet[d] then na[#na+1]=d end end
		win._all=na
		for _,t in ipairs(win._tabs) do t._tdraws={}; t._thbs={}; t._built=false end
		win._scrollY=0
		if win._active then pcall(function() win._active:_build() end) end
		win:_updateScrollbar()
	end
	function win:SetTheme(name) MatchaUI.Theme=MatchaUI.Themes[name] or MatchaUI.Theme; win:_applyTheme() end
	function win:SetAccent(color) if MatchaUI.Theme then MatchaUI.Theme.Accent=color end; win:_applyTheme() end

	-- Whole-UI show/hide (toggle key)
	function win:Hide()
		win._hidden=true
		for _,t in ipairs(win._tabs) do pcall(function() t:_closePopups() end) end
		for _,d in ipairs(win._all) do pcall(function() d.Visible=false end) end
	end
	function win:Show()
		win._hidden=false
		for _,d in ipairs(win._all) do
			local m=META[d]
			if not (m and m.crx) then pcall(function() d.Visible=true end) end
		end
		-- only the active tab keeps its accent bar; the blanket show above lit them all
		for _,t in ipairs(win._tabs) do
			if t._abar then pcall(function() t._abar.Visible=(t==win._active) end) end
		end
		pcall(function() wSbThumb.Visible=false end)
		pcall(function() wTipBg.Visible=false; wTipTx.Visible=false end)
		if win._active then pcall(function() win._active:_refreshContentPos() end) end
		win:_updateScrollbar()
	end
	function win:Toggle() if win._hidden then win:Show() else win:Hide() end end
	function win:SetToggleKey(k)
		if type(k)=="string" then win._toggleKey=KV[k] or win._toggleKey
		elseif type(k)=="number" then win._toggleKey=k end
	end

	-- Resize the window (rebuilds content at the new width/height)
	function win:Resize(w,h)
		WW=math.max(360,flr(w or WW)); WH=math.max(220,flr(h or WH))
		win.ww=WW; win.wh=WH
		pcall(function()
			local vp=workspace.CurrentCamera.ViewportSize
			win.wx=clamp(win.wx,0,math.max(0,vp.X-WW)); win.wy=clamp(win.wy,0,math.max(0,vp.Y-WH))
		end)
		pcall(function()
			wBrd.Size=Vector2.new(WW+2,WH+2); wBg.Size=Vector2.new(WW,WH)
			wBar.Size=Vector2.new(WW,C.TH); wBarB.Size=Vector2.new(WW,8); wTbSep.Size=Vector2.new(WW,1)
			wSide.Size=Vector2.new(C.SW,WH-C.TH); wCont.Size=Vector2.new(WW-C.SW-1,WH-C.TH); wBotMask.Size=Vector2.new(WW-C.SW-1,BOTPAD); M(wBotMask).ry=WH-BOTPAD
		end)
		local ml=M(wSLn); ml.ry2=WH
		M(wClBg).rx=WW-28; M(wClTx).rx=WW-23; M(wMnBg).rx=WW-52; M(wMnTx).rx=WW-48
		closeHb._dRx=WW-28; minHb._dRx=WW-52
		-- Content was laid out for the OLD size (element widths, right-aligned bits and scroll
		-- range are baked in at build time). Tear it down and rebuild at the new W/H. Only the
		-- active tab is rebuilt now; the rest are flagged for lazy rebuild on their next switch.
		if win._active then pcall(function() win._active:_closePopups() end) end
		for i=#win._all,1,-1 do
			local d=win._all[i]; local m=META[d]
			if m and m.crx~=nil then pcall(function() d:Remove() end); table.remove(win._all,i) end
		end
		for _,t in ipairs(win._tabs) do
			t._tdraws={}; t._thbs={}; t._built=false
			for _,sec in ipairs(t._sections) do
				for _,el in ipairs(sec._elements) do el._drawings={}; el._popupDs=nil end
			end
		end
		refreshChrome(); refreshChromeHbs()
		if win._active then
			win._active:_build()
			win._scrollMax=win._active._scrollMax or 0
			win._scrollY=clamp(win._scrollY,0,win._scrollMax)
			win._active:_refreshContentPos(); win._active:_refreshContentHbs()
		end
		win:_applyTheme()
		win:_updateScrollbar()
	end

	-- Built-in Settings tab (library features)
	function win:_addSettingsTab()
		if win._settingsAdded then return end
		win._settingsAdded=true
		local curTheme="Dark"
		for k,v in pairs(MatchaUI.Themes) do if v==MatchaUI.Theme then curTheme=k end end
		local themeNames={}
		for k in pairs(MatchaUI.Themes) do themeNames[#themeNames+1]=k end
		table.sort(themeNames)
		local t=win:Tab({Title="Settings", Icon="settings"})
		local s=t:Section({Title="Interface"})
		s:Dropdown({Title="Theme", Desc="UI color theme", Values=themeNames, Value=curTheme,
			Callback=function(v) win:SetTheme(v) end})
		local sizeMap={["Small"]={480,360},["Default"]={560,420},["Large"]={680,520},["Huge"]={820,600}}
		s:Dropdown({Title="Window Size", Desc="Resize the menu", Values={"Small","Default","Large","Huge"}, Value="Default",
			Callback=function(v) local sz=sizeMap[v]; if sz then win:Resize(sz[1],sz[2]) end end})
		s:Keybind({Title="Menu Toggle Key", Desc="Show / hide this menu", Value=(VK[win._toggleKey] or "RShift"),
			Callback=function(k) win:SetToggleKey(k) end})
		s:Button({Title="Unload UI", Desc="Close and remove the interface", Callback=function() win:Destroy() end})

		-- Configuration (save / load element states)
		local sc=t:Section({Title="Configuration"})
		local nameInput = sc:Input({Title="Config Name", Placeholder="default", Value="default"})
		sc:Button({Title="Save Config", Desc="Save current settings to file", Callback=function()
			local nm=(nameInput.Value~="" and nameInput.Value) or "default"
			local ok=false; pcall(function() ok=win.ConfigManager:Config(nm):Save() end)
			UI:Notify({Title="Config", Content=(ok and "Saved '"..nm.."'" or "Save failed"), Duration=3})
		end})
		sc:Button({Title="Load Config", Desc="Load settings from file", Callback=function()
			local nm=(nameInput.Value~="" and nameInput.Value) or "default"
			pcall(function() win.ConfigManager:Config(nm):Load() end)
			UI:Notify({Title="Config", Content="Loaded '"..nm.."'", Duration=3})
		end})
		local cfgs={}; pcall(function() cfgs=win.ConfigManager:AllConfigs() end)
		if #cfgs>0 then
			sc:Dropdown({Title="Existing Configs", Values=cfgs, Value=cfgs[1], Callback=function(v)
				if nameInput.Set then nameInput:Set(v) end
			end})
		end

		local s3=t:Section({Title="Library"})
		s3:Paragraph({Title="MatchaUI v"..tostring(MatchaUI.Version), Desc="Drawing-based WindUI-style interface for Matcha."})
		return t
	end

	-- Built-in Home tab (landing screen: live debug info + game status)
	function win:_addHomeTab()
		if win._homeAdded then return end
		win._homeAdded=true
		local t=win:Tab({Title="Home", Icon="home"})
		local s2=t:Section({Title=""})
		win._infoBox = s2:InfoBox({ Title="Client Info", Lines={"","","","","","","",""} })
		local ss=t:Section({Title="Supported Games"})
		pcall(function() ss:StatusList({}) end)
		win._startTime = win._startTime or tick()
		task.spawn(function()
			local gameName
			pcall(function() gameName = game:GetService("MarketplaceService"):GetProductInfo(game.PlaceId).Name end)
			local LP = Players.LocalPlayer
			while win._alive and win._infoBox do
				local box=win._infoBox
				local function ping() local p="--"; pcall(function() p=math.floor(LP:GetNetworkPing()*1000+.5).." ms" end); return p end
				local function hp() local v="--"; pcall(function() local h=LP.Character and LP.Character:FindFirstChildOfClass("Humanoid"); if h then v=string.format("%d / %d", math.floor(h.Health), math.floor(h.MaxHealth)) end end); return v end
				local function cam() local v="--"; pcall(function() local p=workspace.CurrentCamera.CFrame.Position; v=string.format("%d, %d, %d", p.X, p.Y, p.Z) end); return v end
				local foc="--"; pcall(function() if isrbxactive then foc=isrbxactive() and "Yes" or "No" end end)
				local up=math.floor(tick()-win._startTime)
				win._fps=win._fps or 0
				pcall(function() box:SetLines({
					"User: "..((LP and (LP.DisplayName or LP.Name)) or "?"),
					"Game: "..(gameName or tostring(game.PlaceId)),
					"Ping: "..ping(),
					"FPS: "..tostring(win._fps),
					"Uptime: "..string.format("%02d:%02d", up//60, up%60),
					"Focused: "..foc,
					"Health: "..hp(),
					"Camera: "..cam(),
				}) end)
				task.wait(1)
			end
		end)
		return t
	end

	-- Destroy
	function win:Destroy()
		win._alive=false
		if win._iConn then pcall(function()win._iConn:Disconnect()end) end
		if win._uisConn then pcall(function()win._uisConn:Disconnect()end) end
		if win._fpsConn then pcall(function()win._fpsConn:Disconnect()end) end
		if win._scrollConn then pcall(function()win._scrollConn:Disconnect()end) end
		-- close dropdown popups
		for _,tab2 in ipairs(win._tabs) do
			for _,sec in ipairs(tab2._sections) do
				for _,el in ipairs(sec._elements) do
					if el._popupDs then for _,d in ipairs(el._popupDs) do pcall(function()d:Remove()end) end end
				end
			end
		end
		for _,d in ipairs(win._all) do pcall(function()d:Remove()end) end
		for i=#MatchaUI._windows,1,-1 do if MatchaUI._windows[i]==win then table.remove(MatchaUI._windows,i) end end
	end

	win.ConfigManager = buildCfgMgr(win)
	MatchaUI._windows[#MatchaUI._windows+1] = win

	-- Built-in Home tab first (landing screen), so it becomes tab #1
	if config.HomeTab~=false then
		pcall(function() win:_addHomeTab() end)
	end

	-- Auto-add the built-in Settings tab after the user's tabs (deferred one frame)
	if config.SettingsTab~=false then
		task.spawn(function() task.wait(0.2); pcall(function() win:_addSettingsTab() end) end)
	end

	-- Real game FPS counter (drives the Settings > Info FPS label if present)
	win._fps=0
	pcall(function()
		local RunService=game:GetService("RunService")
		local frames,last=0,tick()
		win._fpsConn=RunService.RenderStepped:Connect(function()
			if not win._alive then return end
			frames=frames+1
			local n=tick()
			if n-last>=1 then
				win._fps=frames; frames=0; last=n
				if win._fpsLabel and win._fpsLabel.SetValue then pcall(function() win._fpsLabel:SetValue(win._fps) end) end
			end
		end)
	end)

	-- ============================================================
	-- Render / input loop
	-- ============================================================
	task.spawn(function()
		local lmb=false; local drag=false; local dox,doy=0,0; local sldHb=nil
		local sbDrag=false; local sbOff=0
		local keyDown={}; local frameN=0; local tgState=false

		while win._alive do
			task.wait(0.033)
			frameN=frameN+1
			-- menu toggle key (show/hide whole UI)
			if iskeypressed and win._toggleKey then
				local td=iskeypressed(win._toggleKey)
				if td and not tgState and not win._kCapture and not win._iCapture then pcall(function() win:Toggle() end) end
				tgState=td
			end
			if win._hidden then lmb=ismouse1pressed(); continue end
			local m=getMouse(); if not m then continue end
			local mx,my=m.X,m.Y
			local lnow=ismouse1pressed()
			local rise=lnow and not lmb
			local fall=not lnow and lmb

			if rise then
				local inW = mx>=win.wx and mx<=win.wx+WW and my>=win.wy and my<=win.wy+WH
				-- scrollbar thumb grab (highest priority)
				local r=win._sbRect
				if r and mx>=r.x and mx<=r.x2 and my>=r.y and my<=r.y2 then
					sbDrag=true; sbOff=my-r.y
				else
					local handled=false
					if inW then
						-- title bar drag (not on buttons)
						if my>=win.wy and my<=win.wy+C.TH and mx<win.wx+WW-56 then
							drag=true; dox=mx-win.wx; doy=my-win.wy
						end
						-- check window-level hitboxes first (close, min, tab buttons)
						for i=#win._hbs,1,-1 do
							local h=win._hbs[i]
							if mx>=h.x and mx<=h.x2 and my>=h.y and my<=h.y2 then
								pcall(h.fn); handled=true; break
							end
						end
					end
					-- active tab hitboxes -- popup hitboxes (_pop) are tested even OUTSIDE
					-- the window so colorpicker/dropdown panels that render beside the
					-- window are clickable; normal element hitboxes still require inW.
					if not handled and win._active then
						local cTop=win.wy+C.TH; local cBot=win.wy+WH-BOTPAD
						for i=#win._active._thbs,1,-1 do
							local h=win._active._thbs[i]
							local hok = h._pop or (inW and my>=cTop and my<cBot)
							if hok and mx>=h.x and mx<=h.x2 and my>=h.y and my<=h.y2 then
								if h.isDrag then sldHb=h; h.drag(mx)
								else pcall(h.fn) end
								break
							end
						end
					end
				end
			end

			if lnow then
				if drag then
					local vp2=workspace.CurrentCamera.ViewportSize
					local nx=clamp(mx-dox,0,vp2.X-WW); local ny=clamp(my-doy,0,vp2.Y-WH)
					if nx~=win.wx or ny~=win.wy then
						win.wx=nx; win.wy=ny
						refreshChrome(); refreshChromeHbs()
						for _,t in ipairs(win._tabs) do t:_refreshTabHb() end
						if win._active then win._active:_refreshContentPos(); win._active:_refreshContentHbs() end
						win:_updateScrollbar()
					end
				end
				if sbDrag then pcall(function() win:_scrollTo(my, sbOff) end) end
				if sldHb then pcall(sldHb.drag,mx) end
			end

			-- keyboard polling (input typing + keybind capture) — iskeypressed is reliable
			if (win._iCapture or win._kCapture) and iskeypressed then
				local shiftDn = iskeypressed(0x10) or iskeypressed(0xA0) or iskeypressed(0xA1)
				for _,vk in pairs(KV) do
					if vk~=0x01 then  -- allow RMB/MMB/side buttons as binds; LMB is used to click
						local down = iskeypressed(vk)
						if down then
							local held = keyDown[vk] or 0
							local fire = (held==0) or (held>=9 and (held-9)%2==0)
							keyDown[vk]=held+1
							if fire then
								if win._kCapture then
									local fn=win._kCapture; win._kCapture=nil; pcall(fn,vk)
								elseif win._iCapture then
									local el=win._iCapture
									if vk==0x0D or vk==0x1B then
										if el._ibg then pcall(function() el._ibg.Color=MatchaUI.Theme.Button end) end
										win._iCapture=nil
										if el._itx then el._itx.Text=#el.Value>0 and el.Value or el.Placeholder; el._itx.Color=#el.Value>0 and MatchaUI.Theme.Text or MatchaUI.Theme.Placeholder end
										if vk==0x0D then pcall(el.Callback, el.Value) end
									elseif vk==0x08 then
										el.Value=el.Value:sub(1,-2)
										if el._id then MatchaUI.Values[el._id]=el.Value end
									else
										local ch=VC[vk]
										if ch then
											if shiftDn then ch=ch:upper() end
											el.Value=el.Value..ch
											if el._id then MatchaUI.Values[el._id]=el.Value end
										end
									end
								end
							end
						else
							keyDown[vk]=nil
						end
					end
				end
			elseif next(keyDown) then keyDown={} end

			-- keybind activation: fire callback when a bound key is pressed (edge)
			if iskeypressed and not win._kCapture then
				for el in pairs(win._keybinds) do
					local vk=el._vk
					if vk and vk~=0 then
						local down=iskeypressed(vk)
						if down and not el._kdown then pcall(el.Callback, el.Value) end
						el._kdown=down
					end
				end
			end

			-- input cursor blink + live text
			if win._iCapture and win._iCapture._itx then
				local el=win._iCapture
				local cur = ((frameN//15)%2==0) and "|" or ""
				if #el.Value>0 then el._itx.Text=el.Value..cur; el._itx.Color=MatchaUI.Theme.Text
				else el._itx.Text=(cur~="" and cur) or el.Placeholder; el._itx.Color=(cur~="") and MatchaUI.Theme.Text or MatchaUI.Theme.Placeholder end
			end

			-- tooltip on hover (only when idle, not dragging/clicking)
			local tip=nil
			if not lnow and not drag and not sbDrag and not sldHb and not win._minimized and win._active then
				for i=#win._active._thbs,1,-1 do
					local h=win._active._thbs[i]
					if h._tip and mx>=h.x and mx<=h.x2 and my>=h.y and my<=h.y2 then tip=h._tip; break end
				end
			end
			if tip then
				local w=#tip*7+12
				wTipTx.Text=tip
				wTipBg.Size=Vector2.new(w,18)
				local tx0=clamp(mx+14,0,(workspace.CurrentCamera.ViewportSize.X)-w)
				local ty0=my+18
				wTipBg.Position=Vector2.new(flr(tx0),flr(ty0))
				wTipTx.Position=Vector2.new(flr(tx0)+6,flr(ty0)+3)
				wTipBg.Visible=true; wTipTx.Visible=true
			else
				wTipBg.Visible=false; wTipTx.Visible=false
			end

			if fall then drag=false; sldHb=nil; sbDrag=false end
			lmb=lnow
		end
	end)

	-- (Keybind + input capture are handled by iskeypressed polling in the render
	--  loop. UIS.InputBegan is intentionally NOT used: on some builds it reports
	--  Roblox KeyCode values instead of Windows VK, which conflicted with polling.)

	-- Scroll wheel (UIS.InputChanged is not guaranteed to exist in Matcha)
	pcall(function()
		if not UIS.InputChanged then return end
		win._scrollConn = UIS.InputChanged:Connect(function(inp)
			if not win._alive then return end
			local ok,isWheel=pcall(function()
				local t=inp.UserInputType
				return (type(t)=="number" and t==3) or (t and t.Name=="MouseWheel")
			end)
			if ok and isWheel then
				local m=getMouse()
				if m and m.X>=win.wx+C.SW and m.X<=win.wx+WW and m.Y>=win.wy+C.TH and m.Y<=win.wy+WH then
					local dz=0; pcall(function() dz=inp.Position.Z end)
					win._scrollY=clamp(win._scrollY-dz*25, 0, win._scrollMax)
					if win._active then win._active:_refreshContentPos(); win._active:_refreshContentHbs() end
				end
			end
		end)
	end)

	pcall(function() if notify then notify("MatchaUI v"..MatchaUI.Version, "MatchaUI", 3) end end)
	return win
end  -- CreateWindow

-- ============================================================
-- Notify / Popup stubs
-- ============================================================
function MatchaUI:Notify(cfg)
	cfg=cfg or {}
	pcall(notify, cfg.Content or cfg.Desc or "", cfg.Title or "", cfg.Duration or 5)
end

function MatchaUI:Popup(cfg)
	cfg=cfg or {}
	self:Notify({Title=cfg.Title or "Popup", Content=cfg.Content or "", Duration=4})
end

-- Gradient stub (WindUI compatibility — returns nil gracefully)
function MatchaUI:Gradient() return nil end

function MatchaUI:DestroyAll()
	for _,w in ipairs(self._windows or {}) do pcall(function() w:Destroy() end) end
	self._windows = {}
end

-- Matcha's loadstring return value is non-standard, so also expose the
-- library as a global. Loaders can grab it via getgenv().MatchaUI / _G.MatchaUI
-- if `loadstring(game:HttpGet(...))()` does not propagate the return value.
-- On reload, tear down the previous instance's windows so they don't stack.
pcall(function()
	local g = (getgenv and getgenv()) or _G
	if g then
		if g.MatchaUI and g.MatchaUI ~= MatchaUI and g.MatchaUI.DestroyAll then
			pcall(function() g.MatchaUI:DestroyAll() end)
		end
		g.MatchaUI = MatchaUI
	end
end)

return MatchaUI
