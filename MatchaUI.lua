		-- ============================================================
		-- tab:_build — creates all Drawing objects
		-- ============================================================
		function tab:_build()
			if tab._built then return end
			tab._built=true
			local T2=MatchaUI.Theme
			local ew=EW()
			local cy=C.P  -- running Y within content area

			for _,sec in ipairs(tab._sections) do
				-- Section header
