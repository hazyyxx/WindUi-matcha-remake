MatchaUI.IconStroke = nil  -- override stroke thickness in px (default scales with size)
local function resolveVecIcon(name)
	if not MatchaUI.UseVectorIcons then return nil end
	local set = MatchaUI.VectorIcons
	if type(set)~="table" then return nil end
	local _,iconName = name:match("^([%w_]+):(.+)$")
	iconName = iconName or name
	return set[iconName]
end
-- Proxy that quacks like a single Drawing so existing call sites (reg/setRel,
-- refreshChrome, row-fit clip, theme rebuild) work unchanged. It fans
-- Position/Visible/Color/ZIndex/Remove out to its child line & circle drawings.
local VEC_MT = {
	__index = function(t,k)
		if k=="Remove" then
			return function() for _,c in ipairs(rawget(t,"_kids")) do pcall(function() c.d:Remove() end) end end
		elseif k=="Position" then return Vector2.new(rawget(t,"_bx") or 0, rawget(t,"_by") or 0)
		elseif k=="Size" then return Vector2.new(rawget(t,"_w") or 16, rawget(t,"_h") or 16)
		elseif k=="Visible" then return rawget(t,"_vis")
		elseif k=="Color" then return rawget(t,"_col")
		elseif k=="Transparency" then return rawget(t,"_tr") or 0
		elseif k=="ZIndex" then return rawget(t,"_zi") end
		return rawget(t,k)
	end,
	__newindex = function(t,k,v)
		if k=="Position" then
			rawset(t,"_bx",v.X); rawset(t,"_by",v.Y)
			for _,c in ipairs(rawget(t,"_kids")) do
				if c.line then pcall(function()
					c.d.From=Vector2.new(flr(v.X+c.ox),flr(v.Y+c.oy))
					c.d.To  =Vector2.new(flr(v.X+c.ox2),flr(v.Y+c.oy2)) end)
				else pcall(function() c.d.Position=Vector2.new(flr(v.X+c.ox),flr(v.Y+c.oy)) end) end
			end
		elseif k=="Visible" then
			rawset(t,"_vis",v)
			for _,c in ipairs(rawget(t,"_kids")) do pcall(function() c.d.Visible=v end) end
		elseif k=="Color" then
			rawset(t,"_col",v)
			for _,c in ipairs(rawget(t,"_kids")) do pcall(function() c.d.Color=v end) end
		elseif k=="Transparency" then
			rawset(t,"_tr",v)
			for _,c in ipairs(rawget(t,"_kids")) do pcall(function() c.d.Transparency=v end) end
		elseif k=="ZIndex" then
			rawset(t,"_zi",v)
			for _,c in ipairs(rawget(t,"_kids")) do pcall(function() c.d.ZIndex=v end) end
		elseif k=="Size" then
			rawset(t,"_w",v.X); rawset(t,"_h",v.Y)  -- icons are fixed-size; just record
		else rawset(t,k,v) end
	end,
}
local function makeVecIcon(def,x,y,w,h,zi,vis,col)
	local s = (w or 16)/24
	col = col or (MatchaUI.Theme and MatchaUI.Theme.Text) or Color3.new(1,1,1)
	local th = MatchaUI.IconStroke or math.max(1, flr(1.6*s))
	local kids = {}
	if def.p then for _,pl in ipairs(def.p) do
		for i=1,#pl-3,2 do
			local x1,y1,x2,y2 = pl[i]*s,pl[i+1]*s,pl[i+2]*s,pl[i+3]*s
			local d=ln(x+x1,y+y1,x+x2,y+y2,col,th,zi or 55,vis)
			kids[#kids+1]={d=d,line=true,ox=x1,oy=y1,ox2=x2,oy2=y2}
		end
	end end
	if def.c then for _,c in ipairs(def.c) do
		local cx,cy,r = c[1]*s,c[2]*s,c[3]*s
		local d=ci(x+cx,y+cy,r,col,zi or 55,vis)
		pcall(function() d.Filled=false; d.Thickness=th end)
		kids[#kids+1]={d=d,line=false,ox=cx,oy=cy}
	end end
	if #kids==0 then return nil end
	return setmetatable({_vec=true,_kids=kids,_bx=x,_by=y,_w=w,_h=h,_col=col,_zi=zi or 55,_vis=vis~=false}, VEC_MT)
end

-- Create a Drawing.Image. Uses validated raw bytes for URLs/files; for rbxassetids
-- tries fetched bytes (validated) then the rbxassetid string natively.
local function im(src,x,y,w,h,zi,vis)
	if not MatchaUI.IconsEnabled then return nil end
	if type(src)~="string" or src=="" then return nil end
	-- Prefer native vector icons; fall back to PNG/Drawing.Image only if unknown name.
	local vdef = resolveVecIcon(src)
	if vdef then return makeVecIcon(vdef,x,y,w,h,zi,vis) end
	local content, bytes
	local id = src:match("^rbxassetid://(%d+)") or src:match("^rbxasset://(%d+)") or src:match("^(%d+)$")
	if id then content="rbxassetid://"..id
	elseif src:match("^https?://") or (isfile and isfile(src)) then bytes=imgData(src)
	else
		local rid=resolveIcon(src)
		if rid then content=rid; id=rid:match("(%d+)") else bytes=imgData(src) end
	end
	if id then
		local key="asset:"..id
		if _imgCache[key]==nil then _imgCache[key]=fetchAssetBytes(id) or false end
		bytes = bytes or (_imgCache[key] or nil)
	end
	if bytes and not looksImage(bytes) then bytes=nil end  -- don't feed garbage to Drawing.Image
	if not bytes and not content then return nil end
	local d
	local ok=pcall(function() d=Drawing.new("Image") end)
	if not ok or not d then return nil end
	pcall(function() d.Position=Vector2.new(math.floor(x+.5),math.floor(y+.5)) end)
	pcall(function() d.Size=Vector2.new(w,h) end)
	pcall(function() d.ZIndex=zi or 55 end)
	pcall(function() d.Visible=vis~=false end)
	local applied=false
	local props = MatchaUI.ImageDataProp and {MatchaUI.ImageDataProp} or {"Data","Image","Bitmap","ImageData"}
	if bytes then
		for _,prop in ipairs(props) do
			if not applied then pcall(function() d[prop]=bytes; applied=true end) end
		end
	elseif content then
		for _,prop in ipairs({"Image","Uri","Url","Asset","Content","Data"}) do
			if not applied then pcall(function() d[prop]=content; applied=true end) end
		end
	end
	return d
end

