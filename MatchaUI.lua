		local thH=math.max(24, flr(vh*vh/total))
		local trackY=win.wy+C.TH+2
		local travel=vh-thH
		local sy=clamp(win._scrollY,0,sMax)
		local thY=trackY + (sMax>0 and flr(travel*(sy/sMax)) or 0)
